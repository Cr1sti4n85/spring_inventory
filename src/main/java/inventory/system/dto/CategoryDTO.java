@JsonInclude(JsonInclude.Include.NON_NULL)
@JsonIgnoreProperties(ignoreUnknown = true)
public class CategoryDTO {

    private Long id;

    @NotBlank(message = "El nombre es obligatorio")
    private String name;

    private List<ProductDTO> products;

}
